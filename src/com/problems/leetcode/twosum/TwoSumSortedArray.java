    public static int[] twoSum(int[] numbers, int target) {
        int[] ans = new int[2];
        for (int i=0; i<numbers.length; i++) {
            int diff = target - numbers[i];
            int result = binarySearch(numbers, diff, i);
            if(result != -1) {
                ans[0] = i+1;
                ans[1] = result+1;
                break;
            }
        }
        return ans;
    }

    public static int binarySearch(int[] nums, int target, int skip) {
        int start = 0;
        int end = nums.length-1;
        while(start<=end) {
            int mid = start + (end-start)/2;
            if (target == nums[mid] && mid!=skip) {
                return mid;
            } else if (target < nums[mid]) {
                end = mid - 1;
            } else {
                start = mid + 1;
            }
        }
        return -1;
    }

